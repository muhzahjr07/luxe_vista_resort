package com.example.luxevistaresort;

public class NotificationItem {
    public String icon;
    public String title;
    public String message;
    public String time;
    public String type;

    public NotificationItem(String icon, String title, String message, String time, String type) {
        this.icon = icon;
        this.title = title;
        this.message = message;
        this.time = time;
        this.type = type;
    }
}
